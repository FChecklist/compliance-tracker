// Gap-closure fix, 2026-07-09 (AUDIT_2026-07-09.md, Security Assessment
// section). middleware.ts's PROTECTED_APP_ROUTE_PREFIXES has drifted out of
// sync with src/app/(app)/'s real directory listing 4 separate times across
// this project's history -- each time a new module shipped a page directory
// without also remembering to add it to the hand-maintained array. This
// script generates that array directly from the filesystem so the next
// missing route is a build-time fact, not a manually-discovered gap.
//
// Run via `bun run predev`/`prebuild` (see package.json) -- regenerates
// src/lib/protected-routes.generated.ts every time before dev/build.
import { readdirSync, writeFileSync, existsSync } from "node:fs"
import { join, dirname } from "node:path"
import { fileURLToPath } from "node:url"

const __dirname = dirname(fileURLToPath(import.meta.url))
const appDir = join(__dirname, "..", "src", "app", "(app)")
const outFile = join(__dirname, "..", "src", "lib", "protected-routes.generated.ts")

const prefixes = readdirSync(appDir, { withFileTypes: true })
  .filter((entry) => entry.isDirectory() && !entry.name.startsWith("_") && !entry.name.startsWith("."))
  .map((entry) => `/${entry.name}`)
  .sort()

if (prefixes.length === 0) {
  console.error(`generate-protected-routes: found zero directories under ${appDir} -- refusing to write an empty allowlist (would lock every app route open).`)
  process.exit(1)
}

const content = `// AUTO-GENERATED by scripts/generate-protected-routes.mjs -- do not edit by
// hand, it will be overwritten on the next dev/build. Regenerated from the
// real src/app/(app)/ directory listing so middleware.ts's route-protection
// allowlist can never silently drift out of sync with it again (this was a
// real, repeated bug -- 4 separate incidents of new pages shipping without
// the old hand-maintained array being updated).
export const PROTECTED_APP_ROUTE_PREFIXES: string[] = ${JSON.stringify(prefixes, null, 2)}
`

writeFileSync(outFile, content, "utf8")
console.log(`generate-protected-routes: wrote ${prefixes.length} route prefixes to ${outFile}`)
